    def _start(self):
        cnt = _CFG.get("questions_per_round", 5)
        if not self.all_q: return
        self.questions = random.sample(self.all_q, min(cnt, len(self.all_q)))
        self.idx = 0
        self.score = 0
        self.answered = False
        self._show_q()
    def _show_q(self):
        self.answered = False
        for i in reversed(range(self.opts.count())):
            w = self.opts.itemAt(i).widget()
            if w: w.deleteLater()
        q = self.questions[self.idx]
        total = len(self.questions)
        self.prog_lbl.setText(f"Progresso: {self.idx+1}/{total}")
        self.pbar.total = total
        self.pbar.set_progress(self.idx)
        self.cat_lbl.setText(q.get("categoria", "[Geral]"))
        self.q_lbl.setText(q["pergunta"])
        self.fb.setVisible(False)
        self.next_btn.setVisible(False)
        self.opt_btns = []
        for i, op in enumerate(q["opcoes"]):
            b = OptBtn(op, i)
            b.clicked.connect(lambda _, ix=i: self._answer(ix))
            self.opts.addWidget(b)
            self.opt_btns.append(b)
        self.stack.setCurrentIndex(1)
        fade_in_widget(self.stack.widget(1))
    def _answer(self, chosen):
        if self.answered: return
        self.answered = True
        q = self.questions[self.idx]
        correct = q["correta"]
        for i, b in enumerate(self.opt_btns):
            b.setEnabled(False)
            b.set_state("correct" if i == correct else ("wrong" if i == chosen and chosen != correct else "disabled"))
        if chosen == correct: self.score += 1
        txt = q.get("explicacao", "") + (f"\n\n[Atenção]: {q.get('pegadinha', '')}" if q.get("pegadinha") else "")
        self.fb_txt.setText(txt)
        self.fb.setVisible(True)
        self.next_btn.setText("Encerrar" if self.idx == len(self.questions) - 1 else "Avançar")
        self.next_btn.setVisible(True)
        fade_in_widget(self.fb, 250)
        fade_in_widget(self.next_btn, 250)
    def _next(self):
        self.idx += 1
        if self.idx >= len(self.questions): self._result()
        else: self._show_q()
    def _result(self):
        total = len(self.questions)
        pct = self.score / total * 100
        self.r_ic.setText("✓" if pct >= 70 else "✗")
        self.r_sc.setText(f"Score: {self.score}/{total}")
        self.r_mg.setText(f"Status: {'Aprovado' if pct>=70 else 'Reprovado'}  ({pct:.0f}%)")
        self.stack.setCurrentIndex(2)
        fade_in_widget(self.stack.widget(2))

