    def _generate(self):
        key = _CFG.get("gemini_api_key", "")
        if not key:
            self.l_st.setText("Error: API Key missing.")
            self.stack.setCurrentIndex(1)
            QTimer.singleShot(2000, lambda: self.stack.setCurrentIndex(0))
            return
        self.stack.setCurrentIndex(1)
        self.dot.set_active(_c("accent"), True)
        self.l_st.setText("Iniciando conexão...")
        self.work_started.emit()
        self._save_flag = self.save_yes.isChecked()
        cnt = self.ia_spin.value()
        self._worker = GeminiWorker(key, _CFG.get("gemini_model", "gemini-flash-lite-latest"), self.all_q, cnt)
        self._worker.done.connect(self._on_done)
        self._worker.error.connect(self._on_err)
        self._worker.status.connect(lambda s: QTimer.singleShot(0, lambda: self.l_st.setText(s)))
        self._thread = threading.Thread(target=self._worker.run, daemon=True)
        self._thread.start()
    def _on_done(self, qs):
        self.work_finished.emit()
        self.dot.set_active(_c("text_dim"), False)
        self.generated = qs
        self.idx = 0
        self.score = 0
        self.answered = False
        if self._save_flag and qs:
            self.all_q.extend(qs)
            save_questions(self.all_q)
            self.questions_saved.emit(self.all_q)
        QTimer.singleShot(0, self._show_ia_q)
    def _on_err(self, msg):
        self.work_finished.emit()
        self.dot.set_active(_c("red"), False)
        QTimer.singleShot(0, lambda: (self.l_st.setText(f"Exception: {msg[:100]}"), QTimer.singleShot(3500, lambda: self.stack.setCurrentIndex(0))))
    def _show_ia_q(self):
        self.answered = False
        for i in reversed(range(self.ia_opts.count())):
            w = self.ia_opts.itemAt(i).widget()
            if w: w.deleteLater()
        q = self.generated[self.idx]
        total = len(self.generated)
        self.ia_plbl.setText(f"Sessão IA: {self.idx+1}/{total}")
        self.ia_pb.total = total
        self.ia_pb.set_progress(self.idx)
        self.ia_qlbl.setText(q["pergunta"])
        self.ia_fb.setVisible(False)
        self.ia_nb.setVisible(False)
        self.ia_opt_btns = []
        for i, op in enumerate(q["opcoes"]):
            b = OptBtn(op, i)
            b.clicked.connect(lambda _, ix=i: self._ia_ans(ix))
            self.ia_opts.addWidget(b)
            self.ia_opt_btns.append(b)
        self.stack.setCurrentIndex(2)
        fade_in_widget(self.stack.widget(2))
    def _ia_ans(self, chosen):
        if self.answered: return
        self.answered = True
        q = self.generated[self.idx]
        correct = q["correta"]
        for i, b in enumerate(self.ia_opt_btns):
            b.setEnabled(False)
            b.set_state("correct" if i == correct else ("wrong" if i == chosen and chosen != correct else "disabled"))
        if chosen == correct: self.score += 1
        txt = q.get("explicacao", "") + (f"\n\n[Warning]: {q['pegadinha']}" if q.get("pegadinha", "") else "")
        self.ia_ftxt.setText(txt)
        self.ia_fb.setVisible(True)
        self.ia_nb.setText("Encerrar" if self.idx == len(self.generated) - 1 else "Avançar")
        self.ia_nb.setVisible(True)
        fade_in_widget(self.ia_fb, 250)
        fade_in_widget(self.ia_nb, 250)
    def _ia_next(self):
        self.idx += 1
        if self.idx >= len(self.generated): self._ia_result()
        else: self._show_ia_q()
    def _ia_result(self):
        total = len(self.generated)
        pct = self.score / total * 100
        self.ia_ric.setText("✓" if pct >= 70 else "✗")
        self.ia_rsc.setText(f"Score: {self.score}/{total}")
        self.ia_rmg.setText(f"Status: {'Aprovado' if pct>=70 else 'Reprovado'}  ({pct:.0f}%)")
        self.ia_slbl.setText(f"[{len(self.generated)} registros salvos]" if self._save_flag else "[Modo volátil - nada salvo]")
        self.stack.setCurrentIndex(3)
        fade_in_widget(self.stack.widget(3))

